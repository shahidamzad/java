package basic;

public class arithmeticOperation {
    static void main(String[] args) {
        int x = 5 , y = 2 ;
        System.out.println(x + y);
        System.out.println(x - y);
        System.out.println(x * y);
        System.out.println((float) x/y);
        System.out.println((double) x/y);
    }
}
